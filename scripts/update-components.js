// Regenerates the "Components" table in README.md from the actual installed
// versions: Electron's bundled Chromium/Node/V8 (read via process.versions by
// launching the Electron binary headlessly) and the resolved npm dependency
// versions from package-lock.json. Runs automatically on `npm version` (see the
// "version" script in package.json); also runnable directly:
//   node scripts/update-components.js

const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');

const root = path.join(__dirname, '..');
const lock = require(path.join(root, 'package-lock.json'));

function depVersion(name) {
  const entry = lock.packages && lock.packages['node_modules/' + name];
  if (entry && entry.version) return entry.version;
  const pkg = require(path.join(root, 'package.json'));
  const range = (pkg.devDependencies || {})[name] || (pkg.dependencies || {})[name] || '';
  return range.replace(/^[\^~]/, '') || '—';
}

// Get Electron's bundled engine versions straight from process.versions.
function electronVersions() {
  const electronBin = require('electron'); // path to the electron binary
  const tmp = path.join(root, '.components-probe.js');
  fs.writeFileSync(
    tmp,
    "const{app}=require('electron');app.whenReady().then(()=>{console.log('PROBE='+JSON.stringify(process.versions));app.quit()});"
  );
  try {
    const out = execFileSync(electronBin, [tmp], { encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] });
    const line = out.split(/\r?\n/).find((l) => l.startsWith('PROBE='));
    if (!line) throw new Error('no PROBE output from Electron');
    return JSON.parse(line.slice('PROBE='.length));
  } finally {
    fs.unlinkSync(tmp);
  }
}

const v = electronVersions();
const v8 = (v.v8 || '').replace(/-electron.*$/, '');

const table = `<!-- COMPONENTS:START (auto-generated by scripts/update-components.js — do not edit by hand) -->
| Component | Version |
|---|---|
| Electron | ${v.electron} |
| Chromium | ${v.chrome} |
| Node.js (bundled) | ${v.node} |
| V8 | ${v8} |
| electron-builder | ${depVersion('electron-builder')} |
| electron-updater | ${depVersion('electron-updater')} |
<!-- COMPONENTS:END -->`;

const readmePath = path.join(root, 'README.md');
const readme = fs.readFileSync(readmePath, 'utf8');
const re = /<!-- COMPONENTS:START[\s\S]*?<!-- COMPONENTS:END -->/;
if (!re.test(readme)) {
  console.error('Components markers not found in README.md — aborting.');
  process.exit(1);
}
fs.writeFileSync(readmePath, readme.replace(re, table));
console.log(`Components table updated: Electron ${v.electron}, Chromium ${v.chrome}, Node ${v.node}, V8 ${v8}`);
